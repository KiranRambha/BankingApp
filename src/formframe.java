    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        if(nameTextField.getText().equals("") || addressTextArea.getText().equals("") || Long.parseLong(phoneField.getText()) == 0 || sexComboBox.getSelectedItem().toString().equals("NULL") || Double.parseDouble(accountBalanceField.getText()) == 0){
            jOptionPane2.showMessageDialog(this, "fill all the fields");
        }else{
            String s = form.input(nameTextField.getText(), addressTextArea.getText(), Long.parseLong(phoneField.getText()), 
                    branchNameComboBox.getSelectedItem().toString(), sexComboBox.getSelectedItem().toString(), 
                    Double.parseDouble(accountBalanceField.getText()), Double.parseDouble(loanBalanceField.getText()));
            jOptionPane2.showMessageDialog(this, s);
        }
        
    }//GEN-LAST:event_submitButtonMouseClicked

