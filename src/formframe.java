    private void getDetailButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_getDetailButtonMouseClicked
        try{
            form.output(Integer.parseInt(accountNumberField.getText()));
        }catch (Exception e){
            jOptionPane2.showMessageDialog(this, "field empty");
        }
        if(Integer.parseInt(accountNumberField.getText()) == form.getAccountNumber()){
            accountnnumberlabel.setText(String.valueOf(form.getAccountNumber()));
            namelabel.setText(form.getCustomerName());
            addresslabel.setText(form.getCustomerAddress());
            phonelabel.setText(String.valueOf(form.getCustomerPhone()));
            sexlabel.setText(form.getSex());
            accountbalancelabel.setText("$" + String.valueOf(form.getAccountBalace()));
            loanbalancelabel.setText("$" + String.valueOf(form.getLoanBalance()));
            branchidlabel.setText(String.valueOf(form.getBranchId()));
            branchnamelabel.setText(form.getBranchAddress());
            jInternalFrame1.setVisible(true);
        }else{
            accountnnumberlabel.setText("");
            namelabel.setText("");
            addresslabel.setText("");
            phonelabel.setText("");
            sexlabel.setText("");
            accountbalancelabel.setText("$0");
            loanbalancelabel.setText("$0");
            branchidlabel.setText("");
            branchnamelabel.setText("");
            jInternalFrame1.setVisible(true); 
        }
    }//GEN-LAST:event_getDetailButtonMouseClicked
    
    private void totalmenuitemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalmenuitemActionPerformed
        ResultSet s = form.getBankValue();
        Double total = 0.0;
        try{
            while(s.next()){
                total = s.getDouble(1);
            }
        }catch (SQLException e){
            System.out.println(e);
        }
        jOptionPane2.showMessageDialog(this, "Total Value Of the Bank is : $" + Math.round(total));
    }//GEN-LAST:event_totalmenuitemActionPerformed

